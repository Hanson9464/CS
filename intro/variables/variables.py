# intro/variables/variables.py
# Python is dynamically typed

# integer
myvar = 7
print(myvar)

# float
myvar = 7.3
print(myvar)

# string
myvar = "Now I'm a string"
print(myvar)

myvar = "I'm another string"
print(myvar)

myvar = """
This is a very long string.
It has multiple lines.
These new lines will be preserved.
"""
print(myvar)

a, b = 5, 10
print(f"a: {a}\t b: {b}")

a, b = b, a
print(f"a: {a}\t b: {b}")
